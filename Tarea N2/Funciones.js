//Funcion que recibe un string e imprime el string en mayusculas
function mayusculas(entrada1){
    return salida1 = entrada1.toUpperCase();
}
mayusculas("hola mundo");
console.log("Funcion mayusculas:",salida1);

//Funcion que recibe un string e imprime el string en minusculas
function minusculas(entrada2){
    return salida2 = entrada2.toLowerCase();
}
minusculas("Hola Mundo");
console.log("Funcion minusculas:", salida2);

